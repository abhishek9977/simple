package projectBookN;

public class Author 
{
	int authorId;
	String authorName;
	
	void setauthorId(int authId)
	{
		authorId=authId;
	}
	void setauthorName(String authName)
	{
		authorName=authName;
	}
    int getauthorId()
    {
    	return authorId;
    }
    String getauthorName()
    {
    	return authorName;
    }
	
}
